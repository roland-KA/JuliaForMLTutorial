# ╔═╡ cf353269-6f66-49f9-8b84-5c81a7a981bd
md"""
## Preparing the data

Almost all ML models expect the training dataset to be in a tabular form. So the `DataFrame` data type which we used in part I is a good starting point.

In our case we have a so-called classification problem: We want the model to predict the correct glass type when given a set of values (namely our features RI and Na ... Ba; the independent attributes). The model will be trained using data for the independent attributes as well as the corresponding glass type which results in each case. This is called supervised learning. 

For this sort of training we need the data of the independent attributes in tabular form and the resulting types in a separate list. The function `unpack` does this job. It splits the `glass` `DataFrame` in these two components (`XGlass` and `yGlass`). Apart from this it also shuffels the rows using a random number generator which gets initialized with the 'seed' `rng = 123`.
"""

# ╔═╡ b55d1e30-4408-4f52-923a-b44619037505
yGlass, XGlass = unpack(glass, ==(:Type), rng = 123)

# ╔═╡ a53006a4-3df1-4beb-b6b7-f9c2ab579e29
md"""
And indeed, we've got a vector and a table:
"""

# ╔═╡ e7a31074-60cf-4e9c-8097-25f398f23ff2
scitype(yGlass), scitype(XGlass)

# ╔═╡ af84d229-832a-48c8-ace2-defe1bf3b468
md"""
## Choosing a model
"""

# ╔═╡ 179712a4-d8c9-419a-a708-15918d1dfa6e
all_models = models()

# ╔═╡ 608af765-55b4-4412-b293-513a25e35cd8
md"""
Now we have to choose an appropriate model. The `models` function gives us a list of all models MLJ offers. There are currently $(length(all_models)) models in this list, which might be a bit overwhelming.
"""

# ╔═╡ 84fa53e7-f0f5-44c8-a976-b37f9356e9c0
compatible_models = models(matching(XGlass, yGlass))

# ╔═╡ b940caf9-c0fc-43ea-b97d-0df5463bc1a6
md"""
So we should narrow down the list a bit: First we want to see which models are basically 'compatible' with the data types of our dataset. All of our features have *continuous numerical* values and the resulting class (the glass type) is a *nominal* value.

Using `matching(XGlass, yGlass)` with `models` filters exactly those models which fulfill this requirement. This reduces the number considerably to $(length(compatible_models)):
"""

# ╔═╡ 7e41f4e3-a2ae-4b39-aeaf-06c900b83135
md"""
Well, apart from this rather technical 'compatibility' we have a few other expectations to our model: For this tutorial we want a model with high explainability, i.e. it should be easy to understand how it works when classifying some data. And ideally it should be implemented in pure Julia, as it is a tutorial about Julia and ML.

*Decision tree* classifiers have a high level of explainability. So let's have a look, if we can find some in this list which are implemented in Julia.

All models in MLJ are provided with comprehensive meta-information (as can be seen in the following example). E.g. the `docstring` contains a short textual description, `is_pure_julia` tells us, if it is implemented in pure Julia (or just some other programming language with a Julia wrapper around it) and finally each model has a `name`. We can use this meta-information to filter our list of compatible models:
"""

# ╔═╡ 41e0de70-b3a4-43fc-ae35-1bf10a71c371
filter(m -> m.is_pure_julia && contains(m.docstring * m.name, "DecisionTree"),
	compatible_models)

# ╔═╡ 40dc5eaf-fcc2-4803-a3d2-ce72126b77c9
md"""
So we could reduce our list to four candidates. The first and the 4th entry are advanced decision tree models. But we prefer a 'classic' one, which is easy to understand for the purpose of this tutorial. That's why we focus on candidates no. 2 & 3. The `docstring` of theses models contains an URL to their GitHub repository where we can also find the related documentation: No. 2 ist part of the [BetaML package](https://github.com/sylvaticus/BetaML.jl) and no. 3 can be found in the [DecisionTree package](https://github.com/bensadeghi/DecisionTree.jl).

Both implement the well known [CART algorithm](https://en.wikipedia.org/wiki/Decision_tree_learning) and are quite similar, but having a closer look at the documentation, the latter one seems to be more mature und offers a bit more functionality. So we choose this one.
"""

# ╔═╡ 724d8110-5a41-477b-9079-6dee4987ecd4
md"""
## Using a decision tree classifier
"""

# ╔═╡ f665c520-8e26-4762-bf79-f01dada35a47
md"""
A model can be loaded in MLJ using the `@load` macro. It returns a type definition (which we call here `MyDecisionTree`). 
"""

# ╔═╡ deec9d4d-d701-46b2-b578-8a29623af096
MyDecisionTree = @load DecisionTreeClassifier pkg="DecisionTree"

# ╔═╡ 7ac6596a-d24b-4ae7-8339-7076cff64df0
md"""
In the next step we create an instance `dc` of this type. The instance contains all parameters with their default values. So we can see e.g. that the depth of the tree is not limited by default (`max_depth = -1`) and that it won't be pruned after creation (`post_prune = false`).
"""

# ╔═╡ a0f39794-7bfb-42bf-8525-6824d97c36b1
dc = MyDecisionTree()

# ╔═╡ 8a4a5e32-29c5-4d6e-88c9-82afa2ffd7b5
md"""
### Training the model
"""

# ╔═╡ fd954f51-857f-4a99-8029-0fe2b2723cda
md"""
We have now everything together to train our model. First we split our data into a *training data* set and a *test dataset* using a simple holdout strategy (in this case a 70:30 split). I.e. we lay 30% of our data aside for testing and use only 70% for training.

`partition` does this job and shuffels the rows using a random number generator (setting seed to `rng = 123`) before applying the split. As we already did a vertical split of the data into `XGlass` and `yGlass`, these components get splitted seperately into `Xtrain`/`Xtest` and `ytrain`/`ytest` respectively. We inform `partition` about this special situation by setting `multi = true`.
"""

# ╔═╡ 1b408007-5994-4f61-91ed-bb6f1a258114
(Xtrain, Xtest), (ytrain, ytest) = 
	partition((XGlass, yGlass), 0.7, multi = true, rng = 123)

# ╔═╡ e2b31234-8ae3-492b-b61c-9d0bc38adec4
md"""
So `Xtrain, ytrain` contain now 70% of the 'glass' dataset and `Xtest, ytest` the remaining 30%.
"""

# ╔═╡ 9027b99d-2554-4341-b75a-8172932f239f
md"""
Next, we apply the following steps for training and testing:
- First the model and the training data get connected using a so-called *machine*.
- Then the training takes place by calling `fit!` on the machine.
- Afterwards the trained machine can be used to do predictions on new data (using `predict`).
"""

# ╔═╡ 81d68fa4-12dc-4183-8985-e7609febaf0c
md"""
For the sake of simplicity, we restrict the depth of the resulting decision tree to 3:
"""

# ╔═╡ 242c1317-df01-4859-bed5-dd2d3224cf50
dc.max_depth = 3

# ╔═╡ c67b3fb2-e9c7-49cc-a174-88c46c03030d
dc_mach = machine(dc, Xtrain, ytrain)

# ╔═╡ c465abe9-50dc-4080-88d2-fae9a6191eff
fit!(dc_mach)

# ╔═╡ 760d48c9-9d58-4b4c-9eee-d6df12aeec0f
md"""
### Predict and evaluate
"""

# ╔═╡ 78230dbe-87a9-453b-ae23-97ff4ab0d03d
md"""
So let's see how well that model predicts on the test dataset:
"""

# ╔═╡ e4f87f1a-44c1-4928-8d3c-af8f9a5fd2d5
yhat = predict(dc_mach, Xtest)

# ╔═╡ ad76082b-25bd-4a93-b590-2b5db3ea8245
md"""
The predictions in `yhat` don't look perhaps  as you would have expected: `yhat` isn't a list of predicted glass types, but a list of *distributions* stating the predicted probabilities for each possible glass type. This stems from the fact that the selected model (a `DecisionTreeClassifier`) is a *probabilistic model* (as can be seen from it's meta-information: `prediction_type = :probabilistic`). All probabilistic models in MLJ predict distributions (using `Distributions.jl`).
"""

# ╔═╡ 2c441c14-61da-43b5-ac1b-ee957769ce79
md"""
But it's easy to get a list of predicted classes (those with the highest probability within each distribution), just by calling `predict_mode` (instead of `predict`):
"""

# ╔═╡ 8341dc4e-8fd7-4bb5-b1bb-bc31e9a71e92
yhat_classes = predict_mode(dc_mach, Xtest)

# ╔═╡ db2384ea-918c-447a-8a32-28106f753755
md"""
On this basis we can check, how many classes have been predicted correctly by comparing `ytest` (the *correct* classes from the testset) to `yhat_classes` (the predicted classes from our classifier).

Note that we want to compare the *elements* of these two arrays with each other (not the arrays themselves). This can be achieved in Julia using the so-called *broadcast* mechanism which is denoted by using a dot in front of the function applied (in this case a comparison on equality using `==`). So `ytest .== yhat_classes` does an elementwise comparison resulting in a list of boolean values. Finally `count` counts the number of `true` values within this list. 

"""

# ╔═╡ 5753000d-db74-4677-a074-ebd7c22692fd
correct_classes = count(ytest .== yhat_classes)

# ╔═╡ 8e0077ce-1905-4c8e-ba27-0f3a734ab394
accuracy = correct_classes / length(ytest)

# ╔═╡ cb0db38e-7124-4409-972a-a719012a09ad
md"""
Using the result of the count we can see, that $(correct_classes) of the $(length(ytest)) instances in the testset have been predicted correctly. This is a rate of $(accuracy * 100)%.
"""

# ╔═╡ e199f8cc-b2de-4bd5-a077-41d683923cf6
md"""
### Print the decision tree
"""

# ╔═╡ 3d934675-e00c-4cc9-9073-f11baf52109d
md"""
Now it would be nice, if we could see how the decision tree, that has been constructed by our `DecisionTreeClassifier`, looks like. Then we could e.g. check which attributes have been used for branching and at which thresholds that took place.
"""

# ╔═╡ e574f5db-b7cf-4d4f-9803-026a43337f6d
md"""
Fortunately there is a built-in function for this purpose. `report` is a generic function that can be called on a trained machine and delivers (depending on the model used) all relevant information about the trained machine. In case of a decision tree, this 'relevant information' is (among other things) a `TreePrinter` object, which can be called using `print_tree()`.
"""

# ╔═╡ c3a70f7e-bf69-41f8-aecc-9c429d5de1c1
report(dc_mach).print_tree()

# ╔═╡ 9afbdf3c-9bdc-47aa-863a-8cee05013dba
names(glass)

